package AlgoritmosYProgramacion.CuartoSemestre.TercerTrabajo;

public class Node {
    private int tag;
    private char info;
    private Node next;

    public Node(){
        
    }

    public int getTag() {
        return tag;
    }

    public void setTag(int tag) {
        this.tag = tag;
    }

    public char getInfo() {
        return info;
    }

    public void setInfo(char info) {
        this.info = info;
    }

    public Node getNext() {
        return next;
    }

    public void setNext(Node next) {
        this.next = next;
    }

    
}
